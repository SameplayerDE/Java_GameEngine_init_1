package io.github.SameplayerDE.GameEngine.Utils;

public class Vector2 {

    public int X, Y;

    public Vector2(int x, int y) {
        X = x;
        Y = y;
    }

    public int getX() {
        return X;
    }

    public int getY() {
        return Y;
    }

    public void setX(int x) {
        X = x;
    }

    public void setY(int y) {
        Y = y;
    }

}
